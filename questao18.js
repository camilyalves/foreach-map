/*
Exercícios de forEach e map:
Questão:   O que acontece se você chamar map() em um array vazio? 
*/

//Se você chamar map() em um array vazio, o resultado será um novo array vazio. Isso ocorre porque map() sempre retorna um novo array com o mesmo número de elementos que o array original, onde cada elemento é o resultado da função de callback aplicada ao elemento correspondente do array original. Quando o array original está vazio, não há elementos para mapear, então o novo array resultante também será vazio.